def pivot(numbers, start, end):
  if start == end:
    return start
  elif start + 1 == end:
    return start if numbers[start] <= numbers[end] else end
  middle = (int)((start + end) / 2)
  if numbers[middle] > numbers[start] and numbers[middle] > numbers[end]:
    return pivot(numbers, middle, end)
  elif numbers[middle] < numbers[start] and numbers[middle] < numbers[end]:
    return pivot(numbers, start, middle)
  elif numbers[start] == numbers[middle] and numbers[middle] == numbers[end]:
    x = pivot(numbers, start, middle)
    y = pivot(numbers, middle, end)
    if numbers[x] < numbers[x-1]:
      return x
    elif numbers[y] < numbers[y-1]:
      return y
    else:
      return min(x, y)
  elif numbers[start] <= numbers[middle] and numbers[middle] <= numbers[end]:
    return start
  elif numbers[start] == numbers[middle]:
    return pivot(numbers, middle, end)
  elif numbers[middle] == numbers[end]:
    return pivot(numbers, start, middle)

def search_base(numbers, target):
  if len(numbers) == 0:
    return False
  start = 0
  end = len(numbers) - 1
  while (start <= end):
    if start == end:
      return True if numbers[start] == target else False
    elif start + 1 == end:
      return True if numbers[start] == target or numbers[end] == target else False
    middle = (int)((start + end) / 2)
    if numbers[middle] == target:
      return True
    elif numbers[middle] > target:
      end = middle
    else:
      start = middle
  return False

def search(numbers, target):
  p = pivot(numbers, 0, len(numbers)-1)
  first = numbers[:p]
  second = numbers[p:]
  if search_base(first, target):
    return True
  if search_base(second, target):
    return True
  return False

